import { GraphqlError } from '../errors';

export const CommunityBadgeCategory = 'attribute';
export enum CommunityBadgeCategoryType {
  mbti = 'mbti',
  pronoun = 'pronoun',
  chinesezodiac = 'chinesezodiac',
  westernzodiac = 'westernzodiac',
}

export interface CommunityBadgeDefinition {
  communityId: string;
  cta: string;
  displayName: string;
  freeClaim: boolean;
  imgUrl: string;
  pointId: string;
  thumbnailUrl: string;
}

export interface ListCommunityBadgeDefinitionsResponse {
  listCommunityBadgeDefinitions: {
    badges: CommunityBadgeDefinition[];
  } & GraphqlError;
}

export const LIST_COMMUNITY_BADGE_DEFINITIONS = `
query getBadgeDef(
    $category: String!,
    $categoryType: String!,
    $communityId: String!
) {
  listCommunityBadgeDefinitions(
    category: $category
    categoryType: $categoryType
    communityId: $communityId
  ) {
    ... on CommunityBadgeInfoList {
      __typename
      badges {
        communityId
        cta
        displayName
        freeClaim
        imgUrl
        pointId
        thumbnailUrl
      }
    }
    ... on GraphqlError {
      __typename
      error
      message
      statusCode
    }
  }
}
`;
